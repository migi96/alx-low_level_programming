#include "3-calc.h"

/**
 * op_add - the sum of a and b
 * @a: first number
 * @b: second number
 * Return: add.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - the defference of a and b
 * @a: first number
 * @b: second number
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - the product of a and b
 * @a: first number
 * @b: second number
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - the result of the division
 * @a: first number
 * @b: second number
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - the reminder of the division
 * @a: first number
 * @b: second number
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
